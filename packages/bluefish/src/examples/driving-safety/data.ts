// 1950-2011
export const driving_data_per_capita = driving_data
  .filter((d) => d.year >= 1950 && d.year <= 2011)
  .map((d) => {
    const population = US_population.find((p) => p.year === d.year)!.population;
    return {
      year: d.year,
      // auto fatalities per 100,000 people
      fatalities: (d.fatalities * 100_000) / population,
      // vehicle miles driven per capita
      vmt: (d.vmt * 1_000_000) / population,
    };
  })
  .sort((a, b) => a.year - b.year);
