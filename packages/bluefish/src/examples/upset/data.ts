export const genres = Array.from(new Set(data.flatMap((d) => d['genres']))).sort();

// for each genre, count the number of titles that include that genre
export const genreCounts = genres.map((genre) => ({
  genre,
  count: data.filter((d) => d['genres'].includes(genre)).length,
}));

export const hashGenreSet = (genres: string[]) => genres.sort().join(',');

export const dehashGenreSet = (genresHash: string) => genresHash.split(',');

// order-invariant hashes of all sets of genres
export const genreSetHashes = Array.from(new Set(data.map((d) => hashGenreSet(d['genres']))));

// for each set of genres (order invariant), count the number of titles that include exactly those
// genres
export const genreSetCounts = genreSetHashes
  .map((genresHash) => ({
    genres: dehashGenreSet(genresHash),
    count: data.filter((d) => hashGenreSet(d['genres']) === genresHash).length,
  }))
  // .sort((a, b) => b.count - a.count);
  .sort((a, b) => a.count - b.count);

export const genreSetBoundedSize = (min: number, max: number) => {
  return genreSetCounts.filter((d) => d.count >= min && d.count < max);
};
